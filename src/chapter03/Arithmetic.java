package chapter03;

public class Arithmetic {
	public static void main(String[] args) {
		// + - * / % ++ --
		int intVar1 = 10;
		int intVar2 = 30;
		System.out.println(intVar1 + intVar2);
		System.out.println(intVar1 - intVar2);
		System.out.println(intVar1 * intVar2);
		System.out.println(intVar1 / intVar2);
		System.out.println(intVar1 % intVar2);
		intVar1++;
		System.out.println(intVar1);
		intVar2--;
		System.out.println(intVar2);
		
	}
}


