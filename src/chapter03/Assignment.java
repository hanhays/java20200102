package chapter03;

public class Assignment {
	public static void main(String[] args) {
		// = += -= *= /= %=

		int intVar1 = 50;
		intVar1 += 70;
		System.out.println(intVar1);
		
		intVar1 -= 80;
		System.out.println(intVar1);
		
		intVar1 /= 3;
		System.out.println(intVar1);
		
		intVar1 %= 5;
		System.out.println(intVar1);
	}
}






